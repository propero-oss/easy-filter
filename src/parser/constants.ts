/**
 * Token representing the start of filter operator parameters
 * by default it will be mapped to the "(" char
 */
export const TOKEN_PARAMS_START = Symbol("TOKEN_PARAMS_START");
/**
 * Token representing the end of filter operator parameters
 * by default it will be mapped to the ")" char
 */
export const TOKEN_PARAMS_END = Symbol("TOKEN_PARAMS_END");
/**
 * Token representing the separator between filter parameters
 * by default it will be mapped to the "," char
 */
export const TOKEN_PARAMS_SEPARATOR = Symbol("TOKEN_PARAMS_SEPARATOR");
